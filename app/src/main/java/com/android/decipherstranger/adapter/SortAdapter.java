package com.android.decipherstranger.adapter;

import java.util.ArrayList;
import java.util.List;

import android.content.Context;
import android.graphics.Bitmap;
import android.graphics.drawable.BitmapDrawable;
import android.graphics.drawable.Drawable;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.ImageView;
import android.widget.SectionIndexer;
import android.widget.TextView;

import com.android.decipherstranger.R;
import com.android.decipherstranger.entity.User;
import com.android.decipherstranger.util.ChangeUtils;

public class SortAdapter extends BaseAdapter implements SectionIndexer {
    private ArrayList<User> list = null;
    private Context mContext;

	public SortAdapter(Context mContext, ArrayList<User> list) {
		this.mContext = mContext;
		this.list = list;
	}

    /**
     * 当ListView数据发生变化时,调用此方法来更新ListView
     *
     * @param list
     */
    public void updateListView(ArrayList<User> list) {
        this.list = list;
        notifyDataSetChanged();
    }

    public int getCount() {
        return this.list.size();
    }

    public Object getItem(int position) {
        return list.get(position);
    }

    public long getItemId(int position) {
        return position;
    }

    public View getView(final int position, View view, ViewGroup arg2) {
        ViewHolder viewHolder = null;
        final User mContent = list.get(position);
        if (view == null) {
            viewHolder = new ViewHolder();
            view = LayoutInflater.from(mContext).inflate(R.layout.contact_list_item, null);
            viewHolder.tvUserName = (TextView) view.findViewById(R.id.contact_user_name);
            viewHolder.tvLetter = (TextView) view.findViewById(R.id.catalog);
            viewHolder.ivUserPhoto = (ImageView) view.findViewById(R.id.contact_user_photo);
            view.setTag(viewHolder);
        } else {
            viewHolder = (ViewHolder) view.getTag();
        }

        // 根据position获取分类的首字母的Char ascii值
        int section = getSectionForPosition(position);

        // 如果当前位置等于该分类首字母的Char的位置 ，则认为是第一次出现
        if (position == getPositionForSection(section)) {
            viewHolder.tvLetter.setVisibility(View.VISIBLE);
            viewHolder.tvLetter.setText(mContent.getSortLetters());
        } else {
            viewHolder.tvLetter.setVisibility(View.GONE);
        }

        viewHolder.tvUserName.setText(this.list.get(position).getUsername());
        Bitmap photo = null;
        try {
            photo = this.list.get(position).getPortrait();
        } catch (Exception e) {
            e.printStackTrace();
        }
        Drawable drawable = new BitmapDrawable(view.getResources(),photo);
        viewHolder.ivUserPhoto.setImageDrawable(drawable);
        return view;
    }

    final static class ViewHolder {
        TextView tvLetter;
        TextView tvUserName;
        ImageView ivUserPhoto;
    }

    /**
     * 根据ListView的当前位置获取分类的首字母的Char ascii值
     */
    public int getSectionForPosition(int position) {
        return list.get(position).getSortLetters().charAt(0);
    }

    /**
     * 根据分类的首字母的Char ascii值获取其第一次出现该首字母的位置
     */
    public int getPositionForSection(int section) {
        for (int i = 0; i < getCount(); i++) {
            String sortStr = list.get(i).getSortLetters();
            char firstChar = sortStr.toUpperCase().charAt(0);
            if (firstChar == section) {
                return i;
            }
        }

        return -1;
    }

    @Override
    public Object[] getSections() {
        return null;
    }
}